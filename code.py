operators= input("enter an operator (+,-,*,/):")
N1= float(input("enter first number :"))
N2= float(input("enter second number :"))
if operators== "+":
    print(N1+N2)
elif operators== "-":
    print(N1-N2)
elif operators== "*":
    print(N1*N2)
elif operators== "/":
    print(N1/N2)
else:
    print("invalid input")